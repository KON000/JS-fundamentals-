const age = '18';
if (age === 18) {
  console.log('true');
} else {
  console.log('false');
}
// false, '18' != 18

const age2 = 18;
if (age2 === 18) {
  console.log('true');
} else {
  console.log('false');
}
// true, 18 === 18

const age3 = '18';
if (age3 == 18) {
  console.log('true');
} else {
  console.log('false');
}
// true, '18' == 18
